    private void jButton_SearchBstCourierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchBstCourierActionPerformed
        // TODO add your handling code here:

        String location = jComboBox_Location.getSelectedItem().toString();

        //Add import for DB_Access
        try {

            ResultSet rs = dba.getData("select*from delivery_agent where location='" + location + "'  group by location having rating=MAX(rating)");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            int row = dtm.getRowCount();
            for (int i = 0; i < row; i++) {
                dtm.removeRow(0);
            }
            while (rs.next()) {
                Object ob[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7)};
                dtm.addRow(ob);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "SQL Error", "Error", 0);
        }
    }//GEN-LAST:event_jButton_SearchBstCourierActionPerformed

