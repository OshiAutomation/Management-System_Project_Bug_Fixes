    public ArrayList getLocn() {

        ArrayList LocationList = null;
        Connection dbConn = null;

        try {

            dbConn = dbm.connect();

            Statement stmt = dbConn.createStatement();

            String query = "SELECT DISTINCT location  FROM delivery_agent";

            System.out.println(query);
            ResultSet rs = stmt.executeQuery(query);

            LocationList = new ArrayList();

            while (rs.next()) {
                String A_ID = rs.getString(1);
                System.out.println(A_ID);
                LocationList.add(A_ID);
            }

        } catch (SQLException sQLException) {
            System.out.println(sQLException + "-----------Select query failed at JobCatNames");
        } finally {

            //dbm.con_Close(dbConn);
        }
        return LocationList;
    }

