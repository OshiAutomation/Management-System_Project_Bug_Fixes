    public int getOrderID(){
    
         DB_Access dba = new DB_Access();
        int oID=-1;
        
         try {
             ResultSet rst = dba.getData("SELECT fOrderID FROM dropshipping.foreignorder");
             
             rst.last();
                 
             oID=rst.getInt(1);
            
             
   
         } catch (SQLException ex) {
             
             System.out.println(ex.getMessage());
             
         }
         return oID;
    
    
    }
    
    public double CalcShippingCost(String Country){
        DB_Access dba = new DB_Access();
       
        double cost=-1.0;
         try {
             ResultSet rst = dba.getData("SELECT Cost FROM dropshipping.shippingcosts WHERE DestinationCountry='"+Country+"'");
             
             rst.next();
                 
             cost=rst.getDouble("Cost");
            
             
   
         } catch (SQLException ex) {
             
             System.out.println(ex.getMessage());
             
         }
        return cost;
        
    }
    
