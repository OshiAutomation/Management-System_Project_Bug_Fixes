    private void jButton_checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_checkActionPerformed
        jLabel_seid.setVisible(true);
        jLabel1_sname.setVisible(true);
        EmployeeInfo ei = new EmployeeInfo();
        Attend att = new Attend();
        
        String eid = jTextField_eid.getText();
        if(!eid.isEmpty()) {
            if(ei.empAvailable(eid)) {
                jLabel_seid.setText(eid);
                jLabel1_sname.setText(ei.getName(eid));
                if((check == 0)&&(!att.chkAttend(eid))) {
                    att.markCheckIn(eid);              
                }
                else if(check == 1) {
                    att.markCheckOut(eid);
                }  
                else {
                    JOptionPane.showMessageDialog(null,"ID Error");
                }
                
            }
            else {
                JOptionPane.showMessageDialog(null,"Incorrect ID");
            }
        }
        else {
            JOptionPane.showMessageDialog(null,"No ID scanned");
        }
        
        clearAttTable();
        readAttTable();
        jTextField_eid.setText("");
        jTextField_eid.requestFocus();
    }//GEN-LAST:event_jButton_checkActionPerformed

