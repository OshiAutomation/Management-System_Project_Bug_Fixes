    private void jButton_searchLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_searchLActionPerformed
        EmployeeInfo ei = new EmployeeInfo();
        try {
            cleartable();
            String ename = jComboBox_empIDL.getSelectedItem().toString();
            String eid = ei.getempID(ename);
            
            String sql = "select * from riyoma.employee_leaves where empID='"+eid+"'";
            
            DefaultTableModel model = (DefaultTableModel) jTable_leaves.getModel();
            
            ResultSet rs = dba.getData(sql);
            while(rs.next())
            {
                String d1 = rs.getString("empID");
                String d2 = rs.getString("date");
                
                model.addRow(new Object[]{d1,d2});             
            }
        } catch (SQLException ex) {
            Logger.getLogger(Attendance.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"sql error");
        }
    }//GEN-LAST:event_jButton_searchLActionPerformed

    private void jButton_deleteLeaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteLeaveActionPerformed
        EmployeeInfo ei = new EmployeeInfo();
        Attend att = new Attend();

        String eid = ei.getempID(jComboBox_empIDL.getSelectedItem().toString());
        String date = jTextField_dateL.getText();
        if(!date.isEmpty()) {
            int res = JOptionPane.showConfirmDialog(null,"Are you sure you want to continue","Warning",0,2);
            if(res == 0) {
                if(att.deleteLeave(eid, date)) {
                    jComboBox_empIDL.setSelectedIndex(0);
                    jTextField_dateL.setText("");
                    JOptionPane.showMessageDialog(null,"Deleted successfully");
                }
            }
            cleartable();
            readtable();
        }
        else {
            JOptionPane.showMessageDialog(null,"Please search a leave and click on the record to delete");
        }
    }//GEN-LAST:event_jButton_deleteLeaveActionPerformed

