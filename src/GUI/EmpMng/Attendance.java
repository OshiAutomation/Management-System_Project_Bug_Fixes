    private void jButton_reportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_reportActionPerformed
        DBConnManager dbconn = new DBConnManager();
        
        SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy");
        String year = sdf.format(new Date());
        try
        {   
            Connection conn = dbconn.connect();
            JasperDesign jd = JRXmlLoader.load("C:\\\\Management System\\\\src\\\\IReports\\\\Attendance.jrxml");
            String sql = "select a.empID,e.empName,a.month,count(a.empID) as 'Days Present' from riyoma.employee e,riyoma.attendance a where a.date like '"+year+"%' and a.empID=e.empID group by a.empID,a.month";
            
            JRDesignQuery newQuery = new JRDesignQuery();
            newQuery.setText(sql);
            jd.setQuery(newQuery);
            JasperReport jr = JasperCompileManager.compileReport(jd);
           
            JasperPrint jp = JasperFillManager.fillReport(jr, null,conn);
            JasperViewer.viewReport(jp,false); 
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton_reportActionPerformed

