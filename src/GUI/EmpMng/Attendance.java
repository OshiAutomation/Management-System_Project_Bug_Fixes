    private void control() {
        Attend att = new Attend();;
        
        if(att.dayMarked()) {
            if(att.checkInMarked()) {
                jButton_oOut.setEnabled(true);
            }
            else {
                jButton_oOut.setEnabled(false);
            }
            jButton_cOut.setEnabled(false);
            jButton_cIn.setEnabled(false);
            jButton_oIn.setEnabled(false);
            jTextField_eid.setEnabled(false);
            jButton_check.setEnabled(false);
        } 
        else {
            check = 0;
            jTextField_eid.setEnabled(false);
            jButton_check.setEnabled(false);
            jButton_cIn.setEnabled(false);
            jButton_oIn.setEnabled(true);
            jButton_oOut.setEnabled(false);
            jButton_cOut.setEnabled(false);
        }
    }
    
