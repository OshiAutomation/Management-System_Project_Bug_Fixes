    private void readAttTable(){
        try {
            DefaultTableModel model = (DefaultTableModel) jTable_attend.getModel();
            
            String query = "select * from riyoma.attendance a,riyoma.employee e where e.empID=a.empID";
            
            ResultSet rs = dba.getData(query);
            //JOptionPane.showMessageDialog(null,rs1);
            while(rs.next())
            {
                String d1 = rs.getString("empID");
                String d2 = rs.getString("empName");
                String d3 = rs.getString("date");
                String d4 = rs.getString("checkin");
                String d5 = rs.getString("checkout");
                String d6 = rs.getString("otHours");
              
                model.addRow(new Object[]{d1,d2,d3,d4,d5,d6});             
            }
        } catch (SQLException ex) {
           System.out.println(ex + "-----------Select query failed");
        } 
    }
    
