    private void readtable(){
        try {
            DefaultTableModel model = (DefaultTableModel) jTable_leaves.getModel();
            
            String query = "select * from riyoma.employee_leaves";
            
            ResultSet rs = dba.getData(query);
            while(rs.next())
            {
                String d1 = rs.getString("empID");
                String d2 = rs.getString("date");
              
                model.addRow(new Object[]{d1,d2});             
            }
        } catch (SQLException ex) {
           System.out.println(ex + "-----------Select query failed");
        } 
    }
    
    private void cleartable(){
        DefaultTableModel dm = (DefaultTableModel)jTable_leaves.getModel();
        for(int i=0;i<dm.getRowCount();i++) {
            for(int j=0;j<dm.getColumnCount();j++) {
                dm.setValueAt("", i, j);
            }
        }     
        dm.setColumnCount(2);
        dm.setRowCount(0);
    }
    
