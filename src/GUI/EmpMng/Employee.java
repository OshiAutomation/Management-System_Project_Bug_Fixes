    private void cleartable(){
        DefaultTableModel dm = (DefaultTableModel)jTable_emp.getModel();
        for(int i=0;i<dm.getRowCount();i++) {
            for(int j=0;j<dm.getColumnCount();j++) {
                dm.setValueAt("", i, j);
            }
        }     
        dm.setColumnCount(7);
        dm.setRowCount(0);
    }
    
