    private void readtable(){
        try {
            DefaultTableModel model = (DefaultTableModel) jTable_emp.getModel();
            
            String query = "select * from riyoma.employee";
            
            ResultSet rs = dba.getData(query);
            //JOptionPane.showMessageDialog(null,rs1);
            while(rs.next())
            {
                String d1 = rs.getString("empID");
                String d2 = rs.getString("empName");
                String d3 = rs.getString("phone");
                String d4 = rs.getString("address");
                String d5 = rs.getString("email");
                String d6 = rs.getString("designation");
                String d7 = rs.getString("nic");
              
                model.addRow(new Object[]{d1,d2,d3,d4,d5,d6,d7});             
            }
        } catch (SQLException ex) {
           System.out.println(ex + "-----------Select query failed");
        } 
    }
    
