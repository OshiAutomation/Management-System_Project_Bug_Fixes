    public void showModels(ResultSet rst) {
        
        DefaultTableModel dtm= (DefaultTableModel) jTable1.getModel();
        int row=dtm.getRowCount();
            
        for(int i=0; i<row; i++) {
                
            dtm.removeRow(0);    
        }
        
        try {
                
            while(rst.next()) {
                    
                Object ob[]={rst.getString(1), rst.getString(2), rst.getString(3), rst.getString(4)};
                dtm.addRow(ob);    
            }   
            
        } catch (SQLException ex) {
                
            System.out.println(ex + "------------Failure of retriving data");
        }
    }
    
    private void fillComboBox() {
         
         SuppliedOrder so = new SuppliedOrder();
         ArrayList orderIDs = so.getOrderIDs();
        
         Iterator i = orderIDs.iterator();
        
         while(i.hasNext()) {
            
            jComboBox_OrderID.addItem(i.next());
         }
     }
