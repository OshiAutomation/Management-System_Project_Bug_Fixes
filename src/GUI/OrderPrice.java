    private void fillComboBox() {

        ShippingDetails sd = new ShippingDetails();
        ArrayList orderIDs = sd.getShippedOrderIds();

        Iterator i = orderIDs.iterator();

        while (i.hasNext()) {

            jComboBox_OrderID.addItem(i.next());
        }

    }

    private void keyTyped(JTextField textBox) {

        textBox.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                if (!Character.isDigit(e.getKeyChar()) && !(e.getKeyChar() == '.')) {
                    e.consume();
                }
            }
        });
    }

    private void showTable(ResultSet rst) {

        DefaultTableModel dtm = (DefaultTableModel) jTable_ModelPrice.getModel();
        int row = dtm.getRowCount();

        for (int i = 0; i < row; i++) {

            dtm.removeRow(0);
        }

        try {

            while (rst.next()) {

                Object ob[] = {rst.getString(1), rst.getString(2), rst.getString(3)};
                dtm.addRow(ob);
            }

        } catch (SQLException ex) {

            System.out.println(ex + "------------Failure of retriving data");
        }
    }

