    private void jButton_CalcSellingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CalcSellingActionPerformed
        // TODO add your handling code here:

        profitMargin = Double.parseDouble(jTextField_ProfitMrgn.getText().toString());

        Vector<Vector<String>> modelsVector = new Vector<Vector<String>>();
        Vector<String> models = new Vector<String>();
        Vector<Vector<String>> newVector = new Vector<Vector<String>>();

        SuppliedOrder so = new SuppliedOrder();
        modelsVector = so.getModelPrices(orderID);

        for (int i = 0; i < modelsVector.size(); i++) {

            models = modelsVector.get(i);
            String modelName = models.get(0);
            double boughtPrice = Double.parseDouble(models.get(1));
            System.out.println("Model Name is " + modelName);
            System.out.println(boughtPrice);

            double sellingPrice = (boughtPrice * (profitMargin + 100)) / 100;
            System.out.println("Selling Price is " + sellingPrice);
            double sellingProfit = sellingPrice - boughtPrice;

            boolean result = so.addSellingPrice(orderID, modelName, sellingPrice);

            if (!result) {
                JOptionPane.showMessageDialog(this, "Error of sending the selling price of " + modelName + " to the database");
                break;
            }

            models.add(String.valueOf(sellingPrice) + 0);
            models.add(String.valueOf(sellingProfit) + 0);
            newVector.add(models);
        }

        jTable_ModelPrice.setModel(new DefaultTableModel(newVector, headers));
    }//GEN-LAST:event_jButton_CalcSellingActionPerformed

