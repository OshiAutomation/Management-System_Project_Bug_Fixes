    private void InseartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InseartActionPerformed
       
        if (jTextField1_cusName.getText().isEmpty() || jTextField2_company.getText().isEmpty() || jTextField3_email.getText().isEmpty() || jTextField4_address.getText().isEmpty() || jTextField5_phone.getText().isEmpty() || jTextField6_orderId.getText().isEmpty())
        JOptionPane.showMessageDialog(null, "One of the required field is empty!", "Error", JOptionPane.ERROR_MESSAGE);
        
        
       
       try {
         
                String Cust_name = String.valueOf(jTextField1_cusName.getText());
                String company = String.valueOf(jTextField2_company.getText());
                String email = String.valueOf(jTextField3_email.getText());
                String address = String.valueOf(jTextField4_address.getText());
                String phone= String.valueOf(jTextField5_phone.getText());
                //int phone=Integer.parseInt(jTextField5_phone.getText());
                orderid=String.valueOf(jTextField6_orderId.getText());
                
                if(phone.trim().length() > 0 || email.trim().length() > 0){
            
                boolean result=true;
                int atindex=email.indexOf('@');
                int a=0;
               
            for(int i=0;i<phone.length();i++)
            {
                if(Character.isDigit(phone.charAt(i)))
                a++;
            }
            
             if(a!=10)
            {
                JOptionPane.showMessageDialog(null,"Invalid Contact Number!");
                result=false;
            }
             else if(email.lastIndexOf(".")<email.indexOf("@") || atindex<=1 )
            {
                JOptionPane.showMessageDialog(null,"Invalid Email Address!");
                result=false;
            }
               else{

           
              
                dba.setData("insert into Customer_Order(cusOrderID)values ('"+orderid+"')");
                dba.setData("insert into Customer(cusOrderID,cusName,company,phone,email ,address)values('" + orderid+ "','" + Cust_name+ "','" + company + "','" + phone + "','"+ email +"','"+address+"')");
               //DB_Access.setData("insert into Item (cusOrderID)values ('"+order_id+"')");
               JOptionPane.showMessageDialog(null, "Sucsessfully inserted!", null, 2);
               }
        } }
        catch (SQLException ex)
        {
            Logger.getLogger(Order_Process.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "insertion faild", null, 2);
            
            
            
        }
            
       
        
        
    }//GEN-LAST:event_InseartActionPerformed

