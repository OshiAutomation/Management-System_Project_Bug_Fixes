    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        
        if (jTextField6_orderId.getText().isEmpty())
        JOptionPane.showMessageDialog(null, "Enter Order ID to search","Error", JOptionPane.ERROR_MESSAGE);      
         
         orderid=String.valueOf(jTextField6_orderId.getText());
        try {
            ResultSet rs = dba.getData("SELECT cusName, company, email, address, phone FROM customer WHERE cusOrderID='" + orderid + "'");
            
            while(rs.next())
            {
                jTextField1_cusName.setText(rs.getString(1));
                jTextField2_company.setText(rs.getString(2));
                jTextField3_email.setText(rs.getString(3));
                jTextField5_phone.setText(rs.getString(5));
                jTextField4_address.setText(rs.getString(4));
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Order_Process.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SearchActionPerformed

